    private void fillCombo(ClassWidget widget) {
        List<Widget> widgets = widget.getClassDiagramScene().getMainLayer().getChildren();
        for (Widget w : widgets) {
            if (w instanceof InterfaceWidget) {
                cbxTargetInterface.addItem(w);
            }
        }
        lblSourceClass.setText(classWidget.getName());
    }

    public JComboBox<Widget> getComboBoxTarget() {
        return cbxTargetInterface;
    }
